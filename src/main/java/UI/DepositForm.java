    // Confirm
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         // TODO add your handling code here:]
        double amt = Double.parseDouble(jTextField1.getText());
        String currency = (String) jComboBox1.getSelectedItem();
        CurrencyHandler currencyHandler = new CurrencyHandler(currency);
        amt = currencyHandler.convert(amt);

        Account customerAccount = null;
        for (Account account :
                ((Customer) UserController.getInstance().getLoggedInUser()).getAccounts()) {
            if(account.getAccountNumber() == Integer.parseInt(accountNo)) {
                customerAccount = account;
            }
        }
        if(customerAccount == null) {
            Account newAccount = ((Customer) UserController.getInstance().getLoggedInUser()).createAccount(Integer.parseInt(accountNo), accountType, 0, null);
            double fee = amt* Constants.feeRate;
            amt = amt-fee;
//            Transaction feeTransaction = new Transaction(newAccount, null, amt, TransactionType.FEE);
//            feeTransaction.process();
            Transaction transaction = new Transaction(null, newAccount, amt, TransactionType.DEPOSIT);
            transaction.process();
        } else {
            Transaction transaction = new Transaction(null, customerAccount, amt, TransactionType.DEPOSIT);
            transaction.process();
        }
        UserViewAccounts addUpdatePanel = new UserViewAccounts();
        UserDashboard.getSplitPane()
                .setRightComponent(addUpdatePanel);
    }//GEN-LAST:event_jButton1ActionPerformed

